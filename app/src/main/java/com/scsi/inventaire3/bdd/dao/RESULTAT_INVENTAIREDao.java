    int getCOUNT_BY_MISSION_ID(int MISSION_ID);


    @Query("SELECT * FROM RESULTAT_INVENTAIRE where ART_CODE=:ART_CODE and INV_ID=:INV_ID and EMP_CODE=:EMP_CODE and MBR_ZONE=:MBR_ZONE")
    RESULTAT_INVENTAIRE getRESULTAT_EXIST(String ART_CODE, int INV_ID, String EMP_CODE, String MBR_ZONE);

