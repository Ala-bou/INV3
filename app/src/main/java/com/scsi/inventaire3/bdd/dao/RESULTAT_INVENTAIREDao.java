    RESULTAT_INVENTAIRE getRESULTAT_EXIST_WITH_GAMME_LOT(String ART_CODE, String MBR_GAMME1, String MBR_GAMME2, String EMP_CODE, String MBR_ZONE, String MBR_LS_NO);

