    @Query("SELECT * FROM RESULTAT_INVENTAIRE where ART_CODE=:ART_CODE   and MBR_ZONE=:MBR_ZONE and MBR_LS_NO=:MBR_LS_NO")
    RESULTAT_INVENTAIRE getRESULTAT_EXIST_LOT(String ART_CODE,   String MBR_ZONE, String MBR_LS_NO);

